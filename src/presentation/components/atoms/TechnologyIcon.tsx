function normalizeTechnologyName(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9]/g, "");
}

export function TechnologyIcon({
  name,
  iconName,
  className = "",
  size = "md",
}: TechnologyIconProps) {
  // Usar iconName de la base de datos si está disponible, sino normalizar el nombre
  const key = iconName
    ? iconName.toLowerCase()
    : normalizeTechnologyName(name);

  const icon = technologyIcons[key];

  if (!icon) {
    // Si no hay icono, retornar null o un placeholder
    return null;
  }

  return (
    <div
      className={`${iconSizeMap[size]} ${className}`}
      aria-label={name}
      title={name}
    >
      {icon}
    </div>
  );
}

